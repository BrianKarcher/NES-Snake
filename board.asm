.proc draw_board
    lda #>NT0
    sta PPU_ADDRESS
    lda #<NT0
    sta PPU_ADDRESS
    jsr load_board_to_nt
    rts
.endproc

load_board_to_nt:
    lda #<board0
    sta current_low
    lda #>board0
    sta current_high

    lda #<screen
    sta current_low_2
    lda #>screen
    sta current_high_2

    ldx #$0
    ldy #$0

    ; x and y are flip flopped because indirect indexing forces us to use y :(
    fory:
    ldy #$0
    forx:
    ; loop:
    ; Transfer the current memory location to the PPU
    lda (current_low), y
    sta (current_low_2), y ; CPU screen memory
    sta PPU_DATA ; PPU memory
    ; Increment the address
    iny
    jmp checkexit
    noexit:
    cpy #$00 ; 32
    bne forx

    inc current_high
    inc current_high_2
    inx
    cpx #$1e ; 30
    bne fory

    checkexit:
    cpy #$c0
    bne noexit
    cpx #$03
    bne noexit
    ; Done transfering
    rts